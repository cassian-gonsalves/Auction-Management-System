    private void refreshTable()
    {
        int rowCount = viewDeliveryRequest.getRowCount();
        DefaultTableModel model = (DefaultTableModel)viewDeliveryRequest.getModel();
        for(int i = rowCount-1; i>=0; i--)
        {
            model.removeRow(i);
        }
        
        for(DeliveryRequest deliveryRequest: deliveryAdmin.getDeliveryRequests())
        {
                Object row[] = new Object[6];
                row[0] =  deliveryRequest.getCustomer();
                row[1] = deliveryRequest;
                row[2] = deliveryRequest.getPhoneNumber();
                row[3] = String.valueOf(deliveryRequest.getProduct().getId());
                row[4] = deliveryRequest.getProduct();
                row[5] = deliveryRequest.isDelivered()?"Delivered":"Delivery Requested";
                model.addRow(row);
        }
    }
  

