    private void deliverProductButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deliverProductButtonActionPerformed
        int row = viewDeliveryRequest.getSelectedRow();
        if(row<0) {
             JOptionPane.showMessageDialog(null, "Please select a row from the table", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }else{
                          LogRecord.log(Level.INFO, "Delivery Initiated for"+viewDeliveryRequest.getValueAt(row, 1));

        DeliveryRequest deliveryRequest = (DeliveryRequest)viewDeliveryRequest.getValueAt(row, 1);
        deliveryRequest.setDeliveryStatus(true);
        JOptionPane.showMessageDialog(null, "Product delivery initiated", "Warning", JOptionPane.WARNING_MESSAGE);
        refreshTable();
        }
    }//GEN-LAST:event_deliverProductButtonActionPerformed

