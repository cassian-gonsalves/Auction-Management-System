    private void refreshTable()
    {
        int rowCount = dealerViewAuctions.getRowCount();
        DefaultTableModel model = (DefaultTableModel)dealerViewAuctions.getModel();
        for(int i = rowCount-1; i>=0; i--)
        {
            model.removeRow(i);
        }
        
        for(Auction auction: dealer.getAuctionList())
        {
            Object row[] = new Object[2];
            row[0] =  auction.getId();
            row[1] = auction;
            model.addRow(row);
        }
    }
   
    

