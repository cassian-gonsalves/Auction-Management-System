    private void deleteAuctionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteAuctionActionPerformed
        // TODO add your handling code here:      
        int row = dealerViewAuctions.getSelectedRow();
        if(row<0) {
             JOptionPane.showMessageDialog(null, "Please select a row from the table", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Auction auction = (Auction)dealerViewAuctions.getValueAt(row, 1);
        this.dealer.removeAuction(auction);
        refreshTable();
    }//GEN-LAST:event_deleteAuctionActionPerformed

